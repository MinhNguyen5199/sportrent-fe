export class User {
    constructor(firstName, lastName, email, phoneNumber, username, password, token, name, id, role){
      this.firstName = firstName;
      this.lastName = lastName;
      this.email = email;
      this.phoneNumber = phoneNumber;
      this.username = username;
      this.password = password;
      this.token = token;
      this.name = name;
      this.id = id;
      this.role = role;
    }
  }